const filter = document.getElementById('filter');
const result = document.getElementById('result');
const post = document.getElementById('post');
const searchResult = document.getElementById('search-result');
const listItems = [];

filter.addEventListener('input', e => filterData(e.target.value));

const filterData = searchTerm => {
  result.innerHTML = '';

  // search through title, and tags
  if (searchTerm.length >= 2) {
    res = data.filter(item => {
      let searchSynonyms = item.synonyms;
      let searchTitle = item.title;
      let searchDesc = item.description__1;

      if (searchSynonyms != undefined)
        if (
          searchSynonyms.toLowerCase().includes(searchTerm.toLowerCase()) ||
          searchTitle.toLowerCase().includes(searchTerm.toLowerCase()) ||
          searchDesc.toLowerCase().includes(searchTerm.toLowerCase())
        )
          return item;
    });

    searchResult.innerHTML = `Total de produtos encontrados: ${res.length}`;

    res.forEach(product => {
      const imgSrc = slugit(product.title);
      let catSrc = slugit(product.category);
      let catSub = slugit(product.subcategory);
      const div = document.createElement('div');

      if (catSub != '') {
        catSrc = slugit(product.subcategory);
        catSub = '-' + slugit(product.subcategory);
      }
      listItems.push(div);
      div.innerHTML = `

      <a
      href="/products/${imgSrc}${catSub}-${product.id}/"
      class="product-card__link"
      >
<div class="product-card__head">
 

      <picture>
        <source srcset="/images/products/${catSrc}/${imgSrc}-${catSrc}-${product.id}-1.avif" type="image/avif" />                  
          <img src="/images/products/${catSrc}/${imgSrc}-${catSrc}-${product.id}-1.png"
          alt="${product.title}"
          class="product-card__img" decoding="async" loading="lazy"/>
      </picture>
  
</div>

<div class="product-card__body">

  <p class="product-card__title">${product.title}</p>

<p class="product-card__category">${catSrc}</p>

</div> 
</a>

    `;
      div.classList.add('product-card');
      result.appendChild(div);
    });
  }
  if (searchTerm.length < 2) {
    result.innerHTML = '';
    searchResult.innerHTML = '';
  }
};

function slugit(name) {
  return name
    .split(' - ')
    .join('-')
    .split(' + ')
    .join('-')
    .split(' ')
    .join('-')
    .split('/')
    .join('')
    .split(',')
    .join('')
    .split('.')
    .join('')
    .split('&')
    .join('and')
    .split("'")
    .join('')
    .split(':')
    .join('')
    .split('(')
    .join('')
    .split(')')
    .join('')
    .split('%')
    .join('percent')
    .split('é')
    .join('e')

    .toLowerCase();
}
